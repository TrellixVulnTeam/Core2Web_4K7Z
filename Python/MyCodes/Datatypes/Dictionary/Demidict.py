# A dictionary consist of a key-value pairs. Each key value pair maps the key to its aassociated value
# a colon (:) separates each key from its associated value

IplCapt={'MI':"Rohit Sharma",'CSK':"MSDhoni",'RCB':"Virat Kohali"}
print(IplCapt)	#{'MI': 'Rohit Sharma', 'CSK': 'MSDhoni', 'RCB': 'Virat Kohali'}

EmpDetails={"EmpId":25,"CmpName":'Persitent',"Salary":54000.00}
print(EmpDetails)	#{'EmpId': 25, 'CmpName': 'Persitent', 'Salary': 54000.0}


# we can also construct a dictionary with the buit in Dict() function.

GameFame=dict([('cricket','virat'),('football','messi'),('basketball','bryant')])
print(GameFame)	#{'cricket': 'virat', 'football': 'messi', 'basketball': 'bryant'}

