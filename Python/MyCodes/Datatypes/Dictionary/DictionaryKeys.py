# Dictionary keys :

'''
*  Almost any type of values can be used as a dictionary key in python.

*  we can even use built in objects like types and functions.

'''

builtInKey={int:10,float:25.65,bool:True}
print(builtInKey)	#{<class 'int'>: 10, <class 'float'>: 25.65, <class 'bool'>: True}


'''
* Dictionary keys must  be of a type that is immutable.

'''

immutableKey={('6','5'):"shashi bagal",('4','5'):"ajay mehta"}
print(immutableKey)	#{('6', '5'): 'shashi bagal', ('4', '5'): 'ajay mehta'}

