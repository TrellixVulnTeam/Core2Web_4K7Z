# set can be created in two ways :

# 1. built-in set() function :
	
#movieSet=set(<iterable>)

movieSet=set(['kedarnath','chhichhore','MSDhoni'])
print(movieSet)	#{'chhichhore', 'kedarnath', 'MSDhoni'}



#  A set can be created by placing all the items(elements) inside curly braces{} :

mySet={29,5.5,'shashi',('c','c++','java','python','android')}
print(mySet)	#{5.5, ('c', 'c++', 'java', 'python', 'android'), 'shashi', 29}

