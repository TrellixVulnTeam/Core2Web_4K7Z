#Accessing elements from list using their indexes.

lst=[10,20,30,40]
print(lst)	#[10,20,30,40]
print(lst[0])	#10
print(lst[1])	#20
print(lst[2])	#30
print(lst[3])	#40
#print(lst[4])		#indexError:list index out of range

lst1=['Swati',"Sayali",'Pratiksha',"komal"]
print(lst1)
print(lst1[0])	#'swati'
print(lst1[1])	#'sayali'
print(lst1[2])	#'pratiksha'
print(lst1[3])	#'komal'

print(lst1[2:1])	#[]
print(lst1[1][1])	#a

