class RelationalOp{
	public static void main(String[] arg){
		int num1=10,num2=20;
		boolean ans;
		
		ans=num1==num2;
		System.out.println("num1==num2 :"+ans);
		
		ans=num1!=num2;
		System.out.println("num1!=num2 :"+ans);
		
		ans=num1<num2;
		System.out.println("num1<num2 :"+ans);
		
		ans=num1>num2;
		System.out.println("num1>num2 :"+ans);
		
		ans=num1<=num2;
		System.out.println("num1<=num2 :"+ans);
		
		ans=num1>=num2;
		System.out.println("num1=>num2 :"+ans);
		
		
	}
}
