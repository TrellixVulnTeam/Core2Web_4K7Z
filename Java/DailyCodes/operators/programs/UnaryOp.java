class UnaryOp{
	public static void main(String[] arg){
		int num=10;
		
		System.out.println("+num="+(+num));
		System.out.println("-num="+(num));
		System.out.println("++num="+(++num));
		System.out.println("--num="+(--num));
		//System.out.println("!num="+(!num));			.......it requires boolean as datatype
		
	}
}
