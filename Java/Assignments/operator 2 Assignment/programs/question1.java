/*
write a java program which will contain 2 numbers. use the following operations on it
	1.less than(<)
	2.greater than(>)
	3.less than equal to(<=)
	4.greater than equal to(>=)
	5.Equivalent(==)
	6.not equivalent(!=)    
*/

class Question1{
	public static void main(String[] arg){
		int a=10,b=20;
		
		System.out.println("a<b : "+(a<b));	//true
		System.out.println("a>b : "+(a>b));	//false
		System.out.println("a<=b : "+(a<=b));	//true
		System.out.println("a>=b : "+(a>=b));	//false
		System.out.println("a==b : "+(a==b));	//false
		System.out.println("a!=b : "+(a!=b));	//true
		
	}
}
