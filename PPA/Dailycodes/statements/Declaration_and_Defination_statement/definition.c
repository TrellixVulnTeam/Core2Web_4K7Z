//Definition statement

#include<stdio.h>
void main(){
	int a;	//definition
	int b=20;	//definition
	
	printf("a=%d\n",a);	//0 
	printf("a=%d\n",b);	//20
	printf("a=%ld\n",sizeof(a));	//4
	printf("a=%ld\n",sizeof(b));	//4
	
}

/*
    definition statement - the statements which takes space on ram called as definition statement
*/
