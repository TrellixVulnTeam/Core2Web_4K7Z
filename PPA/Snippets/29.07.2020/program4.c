#include<stdio.h>
void main(){
	char chr = 'a';
	printf("It is character = %c",chr);
}

/*
Output:
	It is character = a
------------------------------------------------------------------------------
Exp:
	- printf() function is used to write(display/print) the output on the
	  screen
	  
	In above code :
		chr='a' is character type printf("It is character = %c",chr);
	here the format specifier %c is used to display the character type 
	data.
	
	%c is replaced by value of an char variable(chr). so the printf()
	function display the output on the screen is - It is character= a
*/
